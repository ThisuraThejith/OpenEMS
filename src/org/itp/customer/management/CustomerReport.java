    private void generateRepBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_generateRepBtnActionPerformed
        if (jNIC.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please enter the NIC No", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                if (!Validation.ValidNIC(this.jNIC.getText())) {
                    nicNoLbl.setText("*Invalid NIC No");
                    JOptionPane.showMessageDialog(null, "The NIC No is invalid", "Error", JOptionPane.ERROR_MESSAGE);
                } else if (Validation.ValidNIC(this.jNIC.getText())) {
                    int CusID = 0;
                    Connection con2 = DBconnect.connect();
        //String sqlq="select * from employees where fe1="+jTextField1.getText();
    //        String sqlstsmt = ?;
            //To remove previously added rows
                    String str = jNIC.getText();
                    String sqlstsmt="select * from customer where NIC=? ";
                    
                    
                    try (PreparedStatement pst = con2.prepareStatement(sqlstsmt)) {
                        pst.setString(1, this.jNIC.toString());
                        rs1 = pst.executeQuery();
                        
                        //    Connection connect = new DBconnect(Constants.USER, Constants.PASSWORD).getConnection();
                        //   PreparedStatement preparedStatement = connect.prepareStatement(Queries.EMS.Select.GET_EMPLOYEE_ID_BY_NIC);
                        pst.setString(1, this.jNIC.getText().toString());
                        ResultSet resultset = pst.executeQuery();
                        int count = 0;
                        while (resultset.next()) {
                            CusID = resultset.getInt("CusID");
                            count++;
                        }
                        if (count == 0) {
                            JOptionPane.showMessageDialog(null, "An Customer with this NIC No is not registered", "Error", JOptionPane.ERROR_MESSAGE);
                            return;
                        } else {
                            //int CusId = CusID;
                            ViewIReport.CusReport(CusID);
                        }
                        resultset.close();
                    }
                }

            } catch (SQLException e) {
                System.out.println(e);
            }
        }
    }//GEN-LAST:event_generateRepBtnActionPerformed

