    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String startDate = sd.getText();
        String endDate = ed.getText();
        
        if(!(startDate.isEmpty()) || (!(endDate.isEmpty()))){
            try {
                String qry1 = "select * from sales where Isue_Date between '"+startDate+"' and '"+endDate+"' ";
                pst = (PreparedStatement) con.prepareStatement(qry1);
                rs1 = pst.executeQuery(qry1);
                int tot =0;
                int qun = 0;
                int cost = 0;
                int getprice1 = 0,getprice2,getTotPrice = 0;
                String id;
                while(rs1.next()){
                    tot = tot + Integer.parseInt(rs1.getString("Tot_Amount"));//take total sales
                    qun = Integer.parseInt(rs1.getString("Quantity"));
                    id = rs1.getString("Product_Id");
                    String qry2 = "select getPrice from product_details where Product_Id = '"+id+"'";
                    pst = (PreparedStatement) con.prepareStatement(qry2);
                    rs2 = pst.executeQuery();
                    if(rs2.next()){
                        getprice1 = Integer.parseInt(rs2.getString("getPrice"));
                       
                        //JOptionPane.showMessageDialog(null, getprice);
                    }
                    getprice2 = (getprice1*qun);
                    getTotPrice = getTotPrice + getprice2;//calculate total cost for sales
                }
                //JOptionPane.showMessageDialog(null, tot);
                tc.setText(Integer.toString(getTotPrice));
                ts.setText(Integer.toString(tot));
                np.setText(Integer.toString(tot - getTotPrice));
                
                
                
            } catch (Exception e) {
                System.out.println(e);
            }
        
        }
        else{
            JOptionPane.showMessageDialog(null, "you can not have empty fields");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

